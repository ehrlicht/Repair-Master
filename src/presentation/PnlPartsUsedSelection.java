    protected void loadUsedParts(int repairNo) {
        try {
            usedPartsList = RepairDao.getUsedParts(repairNo);
            displayUsedParts();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void displayUsedParts() {
        partModel.setRowCount(0);
        for (int i = 0; i < usedPartsList.size(); i++) {
            Part p = (Part) usedPartsList.get(i);
            partModel.insertRow(i, new Object[]{p.getDesc(), p.getPrice() + ".-", p.getStock()});
        }
    }

    protected ArrayList getUsedParts() {
        return usedPartsList;
    }

