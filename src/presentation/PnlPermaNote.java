    //Formate une note fournie en param√®tre pour ensuite pouvoir l'afficher correctement.
    protected void setPermaNote(Note n) throws BadLocationException {
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        StyleConstants.setLeftIndent(style, 10);
        StyleConstants.setRightIndent(style, 10);
        StyleConstants.setForeground(style, Color.DARK_GRAY);
        StyleConstants.setFontSize(style, 16);
        document.insertString(document.getLength(), sdf.format(n.getDate()) + " : ", style);
        StyleConstants.setBold(style, true);
        document.insertString(document.getLength(), n.getTitle() + "\n\r", style);
        StyleConstants.setForeground(style, Color.BLACK);
        StyleConstants.setFontSize(style, 14);
        StyleConstants.setBold(style, false);
        StringTokenizer st = new StringTokenizer(n.getBody(), ";");
        while (st.hasMoreTokens()) {
            document.insertString(document.getLength(), st.nextToken() + "\n\r", style);
        }
    }

